const DICTS: Record<Lang, Dict> = {
  ja,
  en,
  zh,
  ko,
  es,
  fr,
  de,
  pt,
  it,
  ru,
  id,
  vi,
  tr,
  pl,
};

export type Translate = (key: string, params?: Record<string, string | number>) => string;

function format(str: string, params?: Record<string, string | number>): string {
  if (!params) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) =>
    params[k] != null ? String(params[k]) : `{${k}}`,
  );
}

export function makeTranslate(lang: Lang): Translate {
  const dict = DICTS[lang] ?? en;
  return (key, params) => {
    const s = format(dict[key] ?? en[key] ?? key, params);
    // macOS では Ctrl 表記を ⌘ に（"Ctrl+N" -> "⌘N"）
    return isMac ? s.replace(/Ctrl\+?/g, "⌘") : s;
  };
}

/** OS の言語から既定言語を推測（未対応なら en）。 */
export function detectLang(): Lang {
  const nav = navigator.language?.toLowerCase() ?? "en";
  const base = nav.split("-")[0];
  const map: Record<string, Lang> = {
    ja: "ja",
    en: "en",
    zh: "zh",
    ko: "ko",
    es: "es",
    fr: "fr",
    de: "de",
    pt: "pt",
    it: "it",
    ru: "ru",
    id: "id",
    vi: "vi",
    tr: "tr",
    pl: "pl",
  };
  return map[base] ?? "en";
}

/** 現在の言語に紐づく t() を返すフック。 */
export function useT(): Translate {
  const lang = useStore((s) => s.settings.lang);
  return useMemo(() => makeTranslate(lang as Lang), [lang]);
}
